package inheritance_samples_oop;

public class AnaClass {
	private int kenar;
	private int kose;
	
	AnaClass(){
		this.kenar=0;
		this.kose=0;
		
	}
	AnaClass(int kenar,int kose){
		this.kenar=kenar;
		this.kose=kose;
	}
	
	
	
	
	
	
	public int getKenar() {
		return kenar;
	}
	public void setKenar(int kenar) {
		this.kenar = kenar;
	}
	public int getKose() {
		return kose;
	}
	public void setKose(int kose) {
		this.kose = kose;
	}
	
	
	
	

}
